package ss1_introduction_to_jave.lab;

public class Manin {
    public static void main(String[] args) {
        int i = 10; //khai báo biến i là kiểu int có giá trị 10
        float f = 20.5f; // khai báo biến f là kiểu float có giá trị 20.5
        double d = 20.5; // khai báo biến d là kiểu double có giá trị 20.5
        boolean b = true; // khai báo biến b là kiểu boolean có giá trị tru
        char c = 'a'; //khai báo biến c là kiểu char có giá trị kí hiệu là a
        String s = "Hà nội"; // khai báo s là kiểu String có giá trị là chuỗi Hà Nội
        System.out.println("i = " + i + "\nf = " + f ); // cách ngắn gọn . \t là vào 1 dòng
        System.out.println("d = "+ d);
        System.out.println("b = "+ b);
        System.out.println("c = "+ c);
        System.out.println("s = "+ s);
    }
}
